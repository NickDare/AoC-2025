package main

import (
	"fmt"
)

const roll = '@'

func partA(input []string) {
	totalRollsFound := 0

	for row := range input {
		for col := 0; col < len(input[row]); col++ {
			if input[row][col] == roll {
				adjRollsOfPaper := 0
				for currentRow := -1; currentRow <= 1; currentRow++ {
					for currentCol := -1; currentCol <= 1; currentCol++ {
						if currentRow == 0 && currentCol == 0 {
							continue
						}
						adjacentRow := row + currentRow
						adjacentCol := col + currentCol
						if adjacentRow >= 0 && adjacentRow < len(input) && adjacentCol >= 0 && adjacentCol < len(input[adjacentRow]) {
							if input[adjacentRow][adjacentCol] == roll {
								adjRollsOfPaper++
							}
						}
					}
				}
				if adjRollsOfPaper < 4 {
					totalRollsFound++
				}
			}
		}
	}

	fmt.Println(totalRollsFound)
}

func checkAndRemoveRolls(input []string) (int, []string) {
	totalRollsRemoved := 0

	for row := range input {
		for col := 0; col < len(input[row]); col++ {
			if input[row][col] == roll {
				adjRollsOfPaper := 0
				for currentRow := -1; currentRow <= 1; currentRow++ {
					for currentCol := -1; currentCol <= 1; currentCol++ {
						if currentRow == 0 && currentCol == 0 {
							continue
						}
						adjacentRow := row + currentRow
						adjacentCol := col + currentCol
						if adjacentRow >= 0 && adjacentRow < len(input) && adjacentCol >= 0 && adjacentCol < len(input[adjacentRow]) {
							if input[adjacentRow][adjacentCol] == roll {
								adjRollsOfPaper++
							}
						}
					}
				}
				if adjRollsOfPaper < 4 {
					totalRollsRemoved++

					runes := []rune(input[row])
					runes[col] = '.'
					input[row] = string(runes)

				}
			}
		}
	}

	return totalRollsRemoved, input

}

func partB(input []string) {

	totalRollsRemoved := 0
	for {
		rollsRemoved, newInput := checkAndRemoveRolls(input)
		if rollsRemoved == 0 {
			break
		}
		totalRollsRemoved += rollsRemoved
		input = newInput
	}

	fmt.Println(totalRollsRemoved)
}

